#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Plot Figure 4.1 — Dataset structural analysis (3-panel figure).

Journal-style design:
  - Unified blue / yellow palette (no decorative call-outs)
  - Panel labels (a) (b) (c) bold, no overall figure title
  - Headline numbers go in the figure CAPTION (not on the chart)
  - Sans-serif font, minimal gridlines

Panel (a):  per-miRNA pos_rate distribution (FULL train)  — histogram
Panel (b):  per-UTR  pos_rate distribution (FULL train)   — histogram
Panel (c):  per-cell-type pos_rate in MIXED pairs         — box plot
            (lymphoid = blue, myeloid = yellow)

Inputs (under runs/_diagnostics/, generated by prepare_fig4_data.py):
    fig4a_per_mirna_pos_rate.csv
    fig4b_per_utr_pos_rate.csv
    fig4c_per_cell_pos_rate_mixed.csv

Outputs:
    runs/_diagnostics/fig4_dataset_analysis.png
    runs/_diagnostics/fig4_dataset_analysis.pdf

Usage:
    python script/prepare_fig4_data.py
    python script/plot_fig4_dataset.py
"""

from __future__ import annotations

import argparse
import csv
import sys
from collections import defaultdict
from pathlib import Path

import numpy as np
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches

SCRIPT_DIR = Path(__file__).resolve().parent
V3_ROOT = SCRIPT_DIR.parent
DIAG_DIR = V3_ROOT / "runs" / "_diagnostics"

# ------------------------------------------------------------------
# Unified palette
# ------------------------------------------------------------------
COLOR_BLUE = "#5B9BD5"      # primary blue (histograms, lymphoid boxes)
COLOR_YELLOW = "#F4C430"    # accent yellow (myeloid boxes)
COLOR_GRID = "#D0D0D0"

# ------------------------------------------------------------------
# Lymphoid / myeloid lineage fallback (used only if CSV `lineage` col missing)
# ------------------------------------------------------------------
LINEAGE_MAP = {
    "CD4": "lymphoid", "CD4_T": "lymphoid",
    "CD8": "lymphoid", "CD8a": "lymphoid", "CD8n": "lymphoid",
    "B": "lymphoid", "B1": "lymphoid", "B2": "lymphoid",
    "plasma": "lymphoid", "Plasma": "lymphoid",
    "NK": "lymphoid", "NK1": "lymphoid", "NK2": "lymphoid",
    "Tregs": "lymphoid", "Treg": "lymphoid", "Tdg": "lymphoid",
    "Gran": "myeloid", "Mono": "myeloid", "ncMono": "myeloid",
    "cDC": "myeloid", "cDC1": "myeloid", "cDC2": "myeloid",
    "pDC": "myeloid", "mDC": "myeloid", "mDC1": "myeloid", "mDC2": "myeloid",
}


def _lineage_of(ct: str) -> str:
    return LINEAGE_MAP.get(ct, "other")


def _load_csv(path: Path) -> list[dict]:
    if not path.exists():
        print(f"[error] not found: {path}", file=sys.stderr)
        print(f"        run prepare_fig4_data.py first:", file=sys.stderr)
        print(f"        python script/prepare_fig4_data.py", file=sys.stderr)
        sys.exit(1)
    with path.open("r", encoding="utf-8") as fh:
        return list(csv.DictReader(fh))


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--out_dir", default=str(DIAG_DIR))
    a = p.parse_args()
    out_dir = Path(a.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    # ---------- Load 3 CSVs ----------
    mirna_rows = _load_csv(out_dir / "fig4a_per_mirna_pos_rate.csv")
    utr_rows = _load_csv(out_dir / "fig4b_per_utr_pos_rate.csv")
    cell_rows = _load_csv(out_dir / "fig4c_per_cell_pos_rate_mixed.csv")

    mirna_pr = np.array([float(r["pos_rate"]) for r in mirna_rows])
    utr_pr = np.array([float(r["pos_rate"]) for r in utr_rows])

    # ---------- Headline numbers (printed to terminal for the CAPTION) ----------
    pct_mirna_narrow = float(np.mean((mirna_pr >= 0.10) & (mirna_pr <= 0.30)))
    pct_utr_extreme = float(np.mean((utr_pr < 0.05) | (utr_pr > 0.95)))

    # ---------- Per-cell_type data for box plot ----------
    # cell_rows = one row per cell_id. Group by cell_type.
    ct_values: dict[str, list[float]] = defaultdict(list)
    ct_lineage: dict[str, str] = {}
    for r in cell_rows:
        ct = r.get("cell_type", "")
        if not ct:
            continue
        ct_values[ct].append(float(r["pos_rate_in_mixed"]))
        ct_lineage[ct] = r.get("lineage", "") or _lineage_of(ct)

    # Order: lymphoid first (desc by mean), then myeloid (desc by mean), then other
    order = {"lymphoid": 0, "myeloid": 1, "other": 2}
    ct_summary = [(ct, np.mean(vs), ct_lineage[ct]) for ct, vs in ct_values.items()]
    ct_summary.sort(key=lambda r: (order[r[2]], -r[1]))
    ct_order = [r[0] for r in ct_summary]
    box_data = [ct_values[ct] for ct in ct_order]
    box_colors = [
        COLOR_BLUE if ct_lineage[ct] == "lymphoid"
        else COLOR_YELLOW if ct_lineage[ct] == "myeloid"
        else "#AAAAAA"
        for ct in ct_order
    ]

    print(f"[load] {len(mirna_pr):,} miRNAs · {len(utr_pr):,} UTRs · "
          f"{len(ct_order)} cell types")
    print(f"[caption-stat] miRNA in [0.10, 0.30] : {pct_mirna_narrow:.1%}")
    print(f"[caption-stat] UTR extreme (<0.05 or >0.95): {pct_utr_extreme:.1%}")
    print(f"[caption-stat] cell-type pos_rate (mixed pairs):")
    for ct, mn, lin in ct_summary:
        print(f"               {ct:>10}  {lin:>9}  mean={mn:.3f}  "
              f"(n={len(ct_values[ct])} cell_ids)")

    # ---------- Plot ----------
    plt.rcParams.update({
        "font.family": "sans-serif",
        "font.sans-serif": ["Arial", "Helvetica", "DejaVu Sans"],
        "axes.linewidth": 0.8,
        "axes.edgecolor": "#333333",
        "axes.labelcolor": "#222222",
        "xtick.color": "#222222",
        "ytick.color": "#222222",
        "xtick.major.width": 0.8,
        "ytick.major.width": 0.8,
    })

    fig, axes = plt.subplots(1, 3, figsize=(15, 4.2),
                              gridspec_kw=dict(wspace=0.30))

    # ---------- Panel (a) ----------
    ax = axes[0]
    bins_a = np.linspace(0, 1, 41)
    ax.hist(mirna_pr, bins=bins_a, color=COLOR_BLUE,
            edgecolor="white", linewidth=0.5)
    ax.set_xlabel("Per-miRNA positive-label rate", fontsize=11)
    ax.set_ylabel("Number of miRNAs", fontsize=11)
    ax.set_xlim(0, 1)
    ax.tick_params(axis="both", which="major", labelsize=9)
    ax.grid(axis="y", color=COLOR_GRID, alpha=0.6, linewidth=0.5)
    ax.set_axisbelow(True)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.text(-0.14, 1.03, "(a)", transform=ax.transAxes,
            fontsize=14, fontweight="bold", va="top")

    # ---------- Panel (b) ----------
    ax = axes[1]
    bins_b = np.linspace(0, 1, 41)
    ax.hist(utr_pr, bins=bins_b, color=COLOR_BLUE,
            edgecolor="white", linewidth=0.5)
    ax.set_xlabel("Per-UTR positive-label rate", fontsize=11)
    ax.set_ylabel("Number of UTRs", fontsize=11)
    ax.set_xlim(0, 1)
    ax.tick_params(axis="both", which="major", labelsize=9)
    ax.grid(axis="y", color=COLOR_GRID, alpha=0.6, linewidth=0.5)
    ax.set_axisbelow(True)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.text(-0.14, 1.03, "(b)", transform=ax.transAxes,
            fontsize=14, fontweight="bold", va="top")

    # ---------- Panel (c) ----------
    ax = axes[2]
    positions = np.arange(1, len(ct_order) + 1)
    bp = ax.boxplot(
        box_data, positions=positions, widths=0.6, patch_artist=True,
        showfliers=False,                          # no outlier dots
        medianprops=dict(color="#000000", linewidth=1.2),
        whiskerprops=dict(color="#333333", linewidth=0.8),
        capprops=dict(color="#333333", linewidth=0.8),
        boxprops=dict(linewidth=0.8),
    )
    for patch, c in zip(bp["boxes"], box_colors):
        patch.set_facecolor(c)
        patch.set_edgecolor("#333333")
        patch.set_alpha(0.95)

    ax.set_xticks(positions)
    ax.set_xticklabels(ct_order, rotation=40, ha="right", fontsize=9)
    ax.set_ylabel("Positive-label rate (mixed pairs)", fontsize=11)
    ax.set_xlabel("Cell type", fontsize=11)
    ax.set_ylim(0, 1)
    ax.tick_params(axis="y", which="major", labelsize=9)
    ax.grid(axis="y", color=COLOR_GRID, alpha=0.6, linewidth=0.5)
    ax.set_axisbelow(True)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.text(-0.18, 1.03, "(c)", transform=ax.transAxes,
            fontsize=14, fontweight="bold", va="top")

    handles = [
        mpatches.Patch(facecolor=COLOR_BLUE, edgecolor="#333333", label="Lymphoid"),
        mpatches.Patch(facecolor=COLOR_YELLOW, edgecolor="#333333", label="Myeloid"),
    ]
    ax.legend(handles=handles, loc="lower left", fontsize=9, frameon=False)

    fig.tight_layout()

    out_png = out_dir / "fig4_dataset_analysis.png"
    out_pdf = out_dir / "fig4_dataset_analysis.pdf"
    fig.savefig(out_png, dpi=300, bbox_inches="tight")
    fig.savefig(out_pdf, bbox_inches="tight")
    print(f"\n[ok] saved: {out_png}")
    print(f"[ok] saved: {out_pdf}")


if __name__ == "__main__":
    main()
